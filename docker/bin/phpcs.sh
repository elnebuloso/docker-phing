#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPCS:=elnebuloso/php-codesniffer:latest}
: ${PHING_PHPCS_EXEC:=phpcs}

##########################################################################################################

_docker-pull-image ${PHING_PHPCS}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPCS} ${PHING_PHPCS_EXEC} "$@

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
