#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPLOC:=elnebuloso/php-phploc:latest}
: ${PHING_PHPLOC_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PHPLOC}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPLOC} ${PHING_PHPLOC_EXEC} "$@

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
