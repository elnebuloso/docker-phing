#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPMD:=elnebuloso/php-phpmd:latest}
: ${PHING_PHPMD_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PHPMD}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPMD} ${PHING_PHPMD_EXEC} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
