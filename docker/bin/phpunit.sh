#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPUNIT:=phpunit/phpunit:latest}
: ${PHING_PHPUNIT_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PHPUNIT}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPUNIT} ${PHING_PHPUNIT_EXEC} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
