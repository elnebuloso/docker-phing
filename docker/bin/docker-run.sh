#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

: ${PHING_VERBOSE_LEVEL:=0}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) $@"

##########################################################################################################

if [[ ${PHING_VERBOSE_LEVEL} -gt 0 ]]; then
    echo "***** running docker: ${run} *****"
fi

${run}

##########################################################################################################
