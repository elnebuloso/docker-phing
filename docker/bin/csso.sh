#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

: ${PHING_CSSO:=elnebuloso/csso-cli:latest}
: ${PHING_CSSO_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_CSSO}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) ${PHING_CSSO} ${PHING_CSSO_EXEC} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
