#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_CLOSURE_COMPILER:=elnebuloso/google-closure-compiler:latest}
: ${PHING_CLOSURE_COMPILER_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_CLOSURE_COMPILER}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_CLOSURE_COMPILER} ${PHING_CLOSURE_COMPILER_EXEC} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
