#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPCBF:=elnebuloso/php-codesniffer:latest}
: ${PHING_PHPCBF_EXEC:=phpcbf}

##########################################################################################################

_docker-pull-image ${PHING_PHPCBF}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPCBF} ${PHING_PHPCBF_EXEC} "$@

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
