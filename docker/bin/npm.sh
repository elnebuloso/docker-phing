#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

: ${PHING_NPM:=node:lts-alpine}
: ${PHING_NPM_CACHE_VOLUME:=phing-npm}
: ${PHING_NPM_CACHE_DIR:=/tmp/npm/cache}
: ${PHING_NPM_EXEC:=npm}

##########################################################################################################

docker-pull ${PHING_NPM}
docker-run --volume ${PHING_NPM_CACHE_VOLUME}-uid-$(id -u):${PHING_NPM_CACHE_DIR} ${PHING_NPM} ${PHING_NPM_EXEC} $@

##########################################################################################################
