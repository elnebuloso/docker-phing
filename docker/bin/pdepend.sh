#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PDEPEND:=elnebuloso/php-pdepend:latest}
: ${PHING_PDEPEND_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PDEPEND}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PDEPEND} ${PHING_PDEPEND_EXEC} "$@

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
