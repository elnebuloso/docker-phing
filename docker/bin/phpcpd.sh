#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: ${PHING_PHPCPD:=elnebuloso/php-phpcpd:latest}
: ${PHING_PHPCPD_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PHPCPD}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --env-file /tmp/env ${PHING_PHPCPD} ${PHING_PHPCPD_EXEC} "$@

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
