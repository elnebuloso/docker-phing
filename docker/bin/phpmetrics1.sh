#!/usr/bin/env bash

##########################################################################################################

set -e

##########################################################################################################

: ${PHING_PHPMETRICS1:=elnebuloso/php-phpmetrics:1}
: ${PHING_PHPMETRICS1_EXEC:=}

##########################################################################################################

_docker-pull-image ${PHING_PHPMETRICS1}

##########################################################################################################

tty=
tty -s && tty=--tty
run="docker run $tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) ${PHING_PHPMETRICS1} ${PHING_PHPMETRICS1_EXEC} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
